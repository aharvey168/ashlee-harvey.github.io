function fizzBuzz() {
  for (let i = 1; i <= 15; i++) {
    // console.log(i);
    if (i % 3 === 0) {
        if (i % 5 === 0 && i % 3 === 0) {
            console.log("fizzbuzz");
        } else {
            console.log("fizz");
        }
    } else if (i % 5 === 0 && i % 3 !== 0) {
        if (i % 5 === 0 && i % 3 === 0) {
            console.log("fizzbuzz");
        } else {
            console.log("buzz");
        }
    } else {
        console.log(i);
    }
 }
}

