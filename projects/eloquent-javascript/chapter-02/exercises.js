// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard() {
  var size = 8; 
  var result = "";
 
  for (var i = 0; i < size; i++) {  
    for (var j = 0; j < size; j++) {
      if ((j + i) % 2 == 0)
        result += " ";
      else
        result += "#";
    }
    result = result + "\n";
  }
 
}

