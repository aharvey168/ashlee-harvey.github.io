function fizzBuzz() {
  let answer;
  for (var i = 1; i <= 15; i++){
    if (i % 3 === 0 && i % 5 === 0) {
      answer = "FizzBuzz";
    } else if (i % 3 === 0) {
      answer = "Fizz";
    } else if (i % 5 === 0) {
      answer = "Buzz";
    } else {
      answer = i;
    }
    console.log(answer);
  }
}
fizzBuzz();

